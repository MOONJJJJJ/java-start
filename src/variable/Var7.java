package variable;

public class Var7 {
    public static void main(String[] args) {
        //리터럴 : 개발자가 직접 변수에 입력해서 넣는 값
        int a  =100;
        double b = 10.5;
        boolean c = true;
        char d = 'A';
        String e = "Hello Java";
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
